'''Módulo con la Función para la multiplicación de dos numeros.
Este archivo contiene la función multiplcación, que realiza la operación del producto entre dos números'''
def multiplicacion(a:float,b:float) -> float: 
    '''Calcula el producto entre dos números. 
    Est función realiza la multiplicacion a*b y retorna el resultado. Puede utilizarse con números enteros o decimales.
    
    Parámetros:
    a (float): Primer número (Factor).
    b (float): Segundo número (Factor).
    
    Returns:
    Devuelve resultado de la multiplicacion de a por b. 
        '''
    return a*b