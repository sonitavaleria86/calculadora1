'''Módulo con la función para sumar dos números.
Este archivo contiene la función suma, que calcula el restulado de sumar dos números'''

def suma(a:float,b:float) -> float:
    '''Devuelve la suma de dos números: a+b.
    Esta función retornal la suma de los valores numéricos a y b. Permite operar con números enteros o 
    decimales, asi como con valores positivos o negativos

    Parámetros:
    a (float): Primer número a sumar 
    b (float): Segundo número a sumar 
    Returns:

    float: Devuelve resultado de la suma de a+b 
        '''
    return a+b 