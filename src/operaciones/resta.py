'''Módulo con la Función para restar dos números.
Este archivo contiene la función resta que calcula la diferencia entre dos números.'''
def resta(a:float,b:float) -> float:
    '''Devuelve la resta de dos números: a-b
    Esta función devuelve la diference entre el primer número (a) y el segundo número (b). Acepta valores enteros
    y decimales, y permite operaciones con números positivos o negativos

    Parámetros
    a(float): Minuendo. Primer número del cual se resta.
    b(float): Sustraendo. Segundo número que se resta al primero. 

    Returns:
    Float: resultado de la operación a-b  
        '''
    return a-b 